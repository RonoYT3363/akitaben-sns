// ===============================
// 秋田弁辞書
// ===============================

// 長い文章を優先
const sentenceDictionary = {
    "ありがとうございます": "ありがどございます",
    "ありがとうございました": "ありがどございました",
    "よろしくお願いします": "よろしぐたのむす",
    "よろしく": "よろしぐ",
    "おはようございます": "おはようさん",
    "こんにちは": "おばんです",
    "こんばんは": "おばんです",
    "今日は寒いですね": "今日はさみなぁ",
    "今日は暑いですね": "今日はあっちぇなぁ",
    "そうですね": "んだな",
    "そうですよ": "んだ"
};

// 単語辞書
const wordDictionary = {
    "ありがとう": "ありがど",
    "こんにちは": "おばんです",
    "こんばんは": "おばんです",
    "おはよう": "おはようさん",
    "はい": "へぇ",
    "いいえ": "んでね",
    "そう": "んだ",
    "そうだ": "んだ",
    "そうです": "んだ",
    "本当": "ほんと",
    "ほんとう": "ほんと",
    "すごい": "しったげ",
    "とても": "しったげ",
    "かなり": "しったげ",
    "疲れた": "しったげ疲れだ",
    "寒い": "さみ",
    "暑い": "あっちぇ",
    "美味しい": "うめ",
    "おいしい": "うめ",
    "だめ": "なんね",
    "ダメ": "なんね",
    "行く": "行ぐ",
    "来る": "来る",
    "食べる": "食う",
    "飲む": "飲む",
    "私": "おら",
    "わたし": "おら",
    "僕": "おら",
    "俺": "おら",
    "あなた": "おめ",
    "君": "おめ"
};

// ===============================
// 秋田弁変換
// ===============================
function convertToAkita(text) {

    let result = text;

    // 長文変換
    for (const [normal, akita] of Object.entries(sentenceDictionary)) {
        result = result.replaceAll(normal, akita);
    }

    // 単語変換
    for (const [normal, akita] of Object.entries(wordDictionary)) {
        result = result.replaceAll(normal, akita);
    }

    return result;
}

// script.jsから使えるようにする
window.convertToAkita = convertToAkita;
